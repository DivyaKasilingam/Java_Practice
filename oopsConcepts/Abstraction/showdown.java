package oopsConcepts.Abstraction;

//Abstract Class vs Interface – The Showdown

public class showdown {

/* 
 *
 * 
Abstract Class:
o	Can have both abstract and concrete methods.
o	Can have instance variables.
o	Can extend only one class (Java doesn’t support multiple inheritance).

Interface:
o	All methods are abstract (Java 8+ allows default and static methods).
o	No instance variables, only constants.
o	A class can implement multiple interfaces (supports multiple inheritance).

When to use:
•	Use an abstract class when there’s a common base functionality, but some behavior must be abstracted.
•	Use an interface when you need to define a contract that multiple classes can implement.

     * 
     */
    
}
